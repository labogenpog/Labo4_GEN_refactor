package ch.heigvd.gen2019;

abstract public class Color {
}

class Yellow extends Color{
    @Override
    public String toString(){
        return "yellow";
    }
}


class Blue extends Color{
    @Override
    public String toString(){
        return "blue";
    }
}


class Red extends Color{
    @Override
    public String toString(){
        return "red";
    }
}